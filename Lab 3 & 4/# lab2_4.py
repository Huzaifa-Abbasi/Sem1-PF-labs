num_int = 42
num_str = str(num_int)
print(f"Integer to String: {num_int} -> {num_str}")


float_str = "3.14"
float_num = float(float_str)
print(f"String to Float: {float_str} -> {float_num}")


float_num = 7.89
int_num = int(float_num)
print(f"Float to Integer: {float_num} -> {int_num}")


